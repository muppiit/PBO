package PBO.uts.no_4;

public class mahasiswa {
    String nim, nama, alamat;
    char jenisKelamin;

    public mahasiswa() {

    }

    public mahasiswa(String nim, String nama, String alamat, char jenisKelamin) {
        this.nim = nim;
        this.nama = nama;
        this.alamat = alamat;
        this.jenisKelamin = jenisKelamin;
    }

}
